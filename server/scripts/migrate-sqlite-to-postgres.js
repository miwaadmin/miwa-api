require('../lib/loadEnv').loadEnv();

const fs = require('fs');
const path = require('path');
const crypto = require('crypto');
const initSqlJs = require('sql.js');
const { Pool } = require('pg');

const repoRoot = path.join(__dirname, '..', '..');
const sqlitePath = process.env.SQLITE_DB_PATH
  || process.env.DB_PATH
  || path.join(repoRoot, 'mftbrain.db');

const dryRun = process.argv.includes('--dry-run');
const schemaOnly = process.argv.includes('--schema-only');
const dataOnly = process.argv.includes('--data-only');
const wipeTarget = process.argv.includes('--wipe-target');

function sslConfig() {
  if (String(process.env.PGSSLMODE || '').toLowerCase() === 'disable') return false;
  return { rejectUnauthorized: false };
}

function ident(name) {
  return `"${String(name).replace(/"/g, '""')}"`;
}

function literal(value) {
  return `'${String(value).replace(/'/g, "''")}'`;
}

function getRows(sqliteDb, sql, params = []) {
  const stmt = sqliteDb.prepare(sql);
  stmt.bind(params);
  const rows = [];
  while (stmt.step()) rows.push(stmt.getAsObject());
  stmt.free();
  return rows;
}

function sqliteDefaultToPostgres(value) {
  if (value === null || value === undefined || value === '') return '';
  const raw = String(value).trim();
  if (/^CURRENT_TIMESTAMP$/i.test(raw)) return ' DEFAULT CURRENT_TIMESTAMP';
  if (/^datetime\('now'\)$/i.test(raw)) return ' DEFAULT CURRENT_TIMESTAMP';
  if (/^\((-?\d+(\.\d+)?)\)$/.test(raw)) return ` DEFAULT ${raw.slice(1, -1)}`;
  if (/^-?\d+(\.\d+)?$/.test(raw)) return ` DEFAULT ${raw}`;
  if (/^'.*'$/.test(raw)) return ` DEFAULT ${raw}`;
  return '';
}

function sqliteTypeToPostgres(type, column) {
  const upper = String(type || '').toUpperCase();
  if (column.pk && upper.includes('INT')) return 'BIGINT GENERATED BY DEFAULT AS IDENTITY';
  if (upper.includes('INT')) return 'INTEGER';
  if (upper.includes('CHAR') || upper.includes('CLOB') || upper.includes('TEXT')) return 'TEXT';
  if (upper.includes('DATE') || upper.includes('TIME')) return 'TIMESTAMPTZ';
  if (upper.includes('REAL') || upper.includes('FLOA') || upper.includes('DOUB')) return 'DOUBLE PRECISION';
  if (upper.includes('BLOB')) return 'BYTEA';
  if (upper.includes('NUM')) return 'NUMERIC';
  return 'TEXT';
}

function buildCreateTable(table, columns) {
  const defs = columns.map((column) => {
    const parts = [ident(column.name), sqliteTypeToPostgres(column.type, column)];
    if (column.pk) parts.push('PRIMARY KEY');
    if (column.notnull && !column.pk) parts.push('NOT NULL');
    if (!column.pk) parts.push(sqliteDefaultToPostgres(column.dflt_value));
    return parts.filter(Boolean).join(' ');
  });
  return `CREATE TABLE IF NOT EXISTS ${ident(table)} (\n  ${defs.join(',\n  ')}\n);`;
}

function getTables(sqliteDb) {
  return getRows(
    sqliteDb,
    "SELECT name FROM sqlite_master WHERE type = 'table' AND name NOT LIKE 'sqlite_%' ORDER BY name"
  ).map((row) => row.name);
}

function getColumns(sqliteDb, table) {
  return getRows(sqliteDb, `PRAGMA table_info(${ident(table)})`);
}

function getPrimaryKeyColumns(sqliteDb, table) {
  return getColumns(sqliteDb, table)
    .filter((column) => Number(column.pk) > 0)
    .sort((a, b) => Number(a.pk) - Number(b.pk))
    .map((column) => column.name);
}

function getUniqueIndexes(sqliteDb, table) {
  return getRows(sqliteDb, `PRAGMA index_list(${ident(table)})`)
    .filter((idx) => Number(idx.unique) === 1)
    .map((idx) => {
      const cols = getRows(sqliteDb, `PRAGMA index_info(${ident(idx.name)})`).map((row) => row.name);
      return { table, name: `uniq_${table}_${cols.join('_')}`, columns: cols };
    })
    .filter((idx) => idx.columns.length > 0);
}

function normalizeForeignKeyAction(action) {
  const upper = String(action || '').trim().toUpperCase();
  if (['CASCADE', 'RESTRICT', 'SET NULL', 'SET DEFAULT', 'NO ACTION'].includes(upper)) return upper;
  return 'NO ACTION';
}

function constraintName(table, id, columns) {
  const base = `fk_${table}_${id}_${columns.join('_')}`.replace(/[^a-zA-Z0-9_]/g, '_');
  if (base.length <= 55) return base;
  return `${base.slice(0, 46)}_${crypto.createHash('sha1').update(base).digest('hex').slice(0, 8)}`;
}

function getForeignKeys(sqliteDb, table) {
  const rows = getRows(sqliteDb, `PRAGMA foreign_key_list(${ident(table)})`);
  const grouped = new Map();

  for (const row of rows) {
    const key = String(row.id);
    if (!grouped.has(key)) {
      grouped.set(key, {
        table,
        id: row.id,
        referencedTable: row.table,
        fromColumns: [],
        toColumns: [],
        onUpdate: normalizeForeignKeyAction(row.on_update),
        onDelete: normalizeForeignKeyAction(row.on_delete),
      });
    }

    const group = grouped.get(key);
    group.fromColumns[Number(row.seq)] = row.from;
    group.toColumns[Number(row.seq)] = row.to;
  }

  return Array.from(grouped.values()).map((fk) => {
    const fallbackPk = getPrimaryKeyColumns(sqliteDb, fk.referencedTable);
    const fromColumns = fk.fromColumns.filter(Boolean);
    const toColumns = fk.toColumns.map((column, index) => column || fallbackPk[index]).filter(Boolean);
    return {
      ...fk,
      fromColumns,
      toColumns,
      name: constraintName(fk.table, fk.id, fromColumns),
    };
  }).filter((fk) => (
    fk.fromColumns.length > 0
      && fk.fromColumns.length === fk.toColumns.length
      && fk.referencedTable
  ));
}

async function createSchema(pg, sqliteDb, tables) {
  for (const table of tables) {
    await pg.query(buildCreateTable(table, getColumns(sqliteDb, table)));
  }

  for (const table of tables) {
    for (const index of getUniqueIndexes(sqliteDb, table)) {
      await pg.query(
        `CREATE UNIQUE INDEX IF NOT EXISTS ${ident(index.name)} ON ${ident(index.table)} (${index.columns.map(ident).join(', ')})`
      );
    }
  }
}

async function constraintExists(pg, name) {
  const result = await pg.query(
    `SELECT 1 FROM pg_constraint WHERE conname = $1 LIMIT 1`,
    [name]
  );
  return result.rowCount > 0;
}

async function addForeignKeys(pg, sqliteDb, tables) {
  const tableSet = new Set(tables);

  for (const table of tables) {
    for (const fk of getForeignKeys(sqliteDb, table)) {
      if (!tableSet.has(fk.referencedTable)) continue;
      if (await constraintExists(pg, fk.name)) continue;

      await pg.query(
        `ALTER TABLE ${ident(fk.table)}
           ADD CONSTRAINT ${ident(fk.name)}
           FOREIGN KEY (${fk.fromColumns.map(ident).join(', ')})
           REFERENCES ${ident(fk.referencedTable)} (${fk.toColumns.map(ident).join(', ')})
           ON UPDATE ${fk.onUpdate}
           ON DELETE ${fk.onDelete}
           DEFERRABLE INITIALLY DEFERRED`
      );
    }
  }
}

async function wipeSchema(pg, tables) {
  if (!tables.length) return;
  await pg.query(`DROP TABLE IF EXISTS ${tables.map(ident).join(', ')} CASCADE`);
}

async function copyTable(pg, sqliteDb, table) {
  const columns = getColumns(sqliteDb, table).map((column) => column.name);
  if (!columns.length) return 0;

  const order = columns.includes('id') ? ' ORDER BY id' : '';
  const rows = getRows(sqliteDb, `SELECT * FROM ${ident(table)}${order}`);
  if (!rows.length) return 0;

  const colSql = columns.map(ident).join(', ');
  const placeholders = columns.map((_, i) => `$${i + 1}`).join(', ');
  const insertSql = `INSERT INTO ${ident(table)} (${colSql}) VALUES (${placeholders})`;

  for (const row of rows) {
    await pg.query(insertSql, columns.map((column) => row[column]));
  }

  return rows.length;
}

async function resetIdentity(pg, sqliteDb, table) {
  const pk = getColumns(sqliteDb, table).find((column) => column.pk && /INT/i.test(column.type || ''));
  if (!pk) return;
  const result = await pg.query(`SELECT pg_get_serial_sequence(${literal(table)}, ${literal(pk.name)}) AS seq`);
  const seq = result.rows[0]?.seq;
  if (!seq) return;
  await pg.query(
    `SELECT setval($1, COALESCE((SELECT MAX(${ident(pk.name)}) FROM ${ident(table)}), 0) + 1, false)`,
    [seq]
  );
}

async function copyData(pg, sqliteDb, tables) {
  const counts = {};
  await pg.query('BEGIN');
  try {
    for (const table of tables) {
      counts[table] = await copyTable(pg, sqliteDb, table);
    }
    for (const table of tables) {
      await resetIdentity(pg, sqliteDb, table);
    }
    await pg.query('COMMIT');
  } catch (err) {
    await pg.query('ROLLBACK');
    throw err;
  }
  return counts;
}

async function main() {
  if (!fs.existsSync(sqlitePath)) {
    throw new Error(`SQLite database not found at ${sqlitePath}`);
  }
  if (!dryRun && !process.env.DATABASE_URL) {
    throw new Error('DATABASE_URL is required unless --dry-run is used.');
  }
  if (!dryRun && process.env.MIGRATION_CONFIRM !== 'copy-miwa-sqlite-to-postgres') {
    throw new Error('Set MIGRATION_CONFIRM=copy-miwa-sqlite-to-postgres to run this PHI data migration.');
  }
  if (schemaOnly && dataOnly) {
    throw new Error('Use either --schema-only or --data-only, not both.');
  }

  const SqlJs = await initSqlJs();
  const sqliteDb = new SqlJs.Database(fs.readFileSync(sqlitePath));
  const tables = getTables(sqliteDb);

  if (dryRun) {
    const summary = tables.map((table) => ({
      table,
      columns: getColumns(sqliteDb, table).length,
      rows: getRows(sqliteDb, `SELECT COUNT(*) AS count FROM ${ident(table)}`)[0]?.count || 0,
    }));
    console.log(JSON.stringify({ status: 'dry-run', sqlitePath, tables: summary }, null, 2));
    sqliteDb.close();
    return;
  }

  const pool = new Pool({
    connectionString: process.env.DATABASE_URL,
    ssl: sslConfig(),
    max: 4,
  });

  try {
    if (wipeTarget) await wipeSchema(pool, tables);
    if (!dataOnly) await createSchema(pool, sqliteDb, tables);
    const counts = schemaOnly ? {} : await copyData(pool, sqliteDb, tables);
    if (!dataOnly) await addForeignKeys(pool, sqliteDb, tables);
    console.log(JSON.stringify({
      status: 'ok',
      provider: 'azure-postgresql',
      sqlitePath,
      tables: tables.length,
      copiedRows: counts,
    }, null, 2));
  } finally {
    sqliteDb.close();
    await pool.end();
  }
}

if (require.main === module) {
  main().catch((err) => {
    console.error(JSON.stringify({
      status: 'error',
      provider: 'azure-postgresql',
      message: err.message,
    }, null, 2));
    process.exit(1);
  });
}

module.exports = {
  buildCreateTable,
  constraintName,
  getForeignKeys,
  normalizeForeignKeyAction,
  sqliteDefaultToPostgres,
  sqliteTypeToPostgres,
};
